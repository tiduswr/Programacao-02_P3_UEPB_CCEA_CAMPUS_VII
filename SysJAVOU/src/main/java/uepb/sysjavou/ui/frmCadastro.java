    private void btAddContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddContaActionPerformed
        DefaultTableModel model = (DefaultTableModel) this.tblContas.getModel();
        Cliente c;
        
        String agencia = ui.askTextOnlyNumbers("Por favor digite a Agência!", "Digite apenas Numeros!");
        String conta = ui.askTextOnlyNumbers("Por favor digite a Conta!", "Digite apenas Numeros!");
        c = banco.getClientebyConta(Integer.parseInt(agencia), Integer.parseInt(conta));
        
        if(c == null){
            boolean teste = false;
            
            for(int i = 0; i < model.getRowCount(); i++){
                if(String.valueOf(model.getValueAt(i, 0)).equals(String.valueOf(agencia)) &&
                String.valueOf(model.getValueAt(i, 1)).equals(String.valueOf(conta))){
                    teste = true;
                }
            }
            if(!teste){
               contas.add(new Conta(Integer.parseInt(agencia), Integer.parseInt(conta)));
                model.addRow(new Object[] {agencia, conta});
                this.tblContas.setModel(model); 
            }else{
                ui.showMsg("Essa conta ja esta cadastrada!");
            }
            
        }else{
            ui.showMsg("Essa conta ja esta cadastrada!");
        }
    }//GEN-LAST:event_btAddContaActionPerformed

    private void btRemoveContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoveContaActionPerformed
        DefaultTableModel model = (DefaultTableModel) this.tblContas.getModel();
        
        String agencia = ui.askTextOnlyNumbers("Por favor digite a Agência!", "Digite apenas Numeros!");
        String conta = ui.askTextOnlyNumbers("Por favor digite a Conta!", "Digite apenas Numeros!");
        int i = 0;
        
        for(i = 0; i < this.contas.size(); i++){
            if(this.contas.get(i).getAgencia() == Integer.parseInt(agencia) &&
                    this.contas.get(i).getNumeroConta() == Integer.parseInt(conta)){
                this.contas.remove(i);
                i++;
            }
        }
        for(i = 0; i < this.tblContas.getRowCount(); i ++){
            if(String.valueOf(model.getValueAt(i, 0)).equals(String.valueOf(agencia)) && 
                    String.valueOf(model.getValueAt(i, 1)).equals(String.valueOf(conta))){
                model.removeRow(i);
            }
        }
        
        this.tblContas.setModel(model);
    }//GEN-LAST:event_btRemoveContaActionPerformed
    

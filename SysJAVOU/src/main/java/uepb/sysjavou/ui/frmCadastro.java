    public void showForm(Cliente cliente){
        this.cliente = cliente;
        this.action = -1;
        
        Object[][] obj;
        int i = 0;
        
        if(cliente != null){
            this.contas = cliente.getContas();
            obj = new Object[cliente.getContas().size()][2];
            for(Conta c : cliente.getContas()){
                obj[i][0] = c.getAgencia();
                obj[i][1] = c.getNumeroConta();
                i++;
            }
            this.tblContas.setModel(new DefaultTableModel(obj, new String[] {"Agencia", "Conta"}));
            i = 0;
            
            this.txtbCPF.setText(cliente.getCpf());
            this.txtbCelular.setText(cliente.getContato().getCelularSemFormatacao());
            this.txtbEmail.setText(cliente.getContato().getEmail());
            this.txtbIdade.setText(String.valueOf(cliente.getIdade()));
            this.txtbLog.setText(cliente.getEndereco().getLogradouro());
            this.txtbLogBairro.setText(cliente.getEndereco().getBairro());
            this.txtbLogCidade.setText(cliente.getEndereco().getCidade());
            this.txtbLogEstado.setText(cliente.getEndereco().getUf());
            this.txtbLogNumero.setText(String.valueOf(cliente.getEndereco().getNumero()));
            this.txtbLogPais.setText(cliente.getEndereco().getPais());
            this.txtbLogTipo.setText(cliente.getEndereco().getTipoLogradouro());
            this.txtbNome.setText(cliente.getNome());
            this.txtbRedeSocial.setText(cliente.getContato().getRedeSocial());
            this.txtbTelefone.setText(cliente.getContato().getTelefoneSemFormatacao());
            
        }else{
            this.contas = new ArrayList();
            this.tblContas.setModel(new DefaultTableModel(null, new String[] {"Agencia", "Conta"}));
        }
        this.setLocationRelativeTo(null);
        this.setModal(true);
        this.setVisible(true);
    }
